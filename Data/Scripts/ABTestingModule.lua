-- @param int
-- @return tbl binary EX => {1, 0, 1, 0, 0}
function Api.ConvertPlayerIdIntoTestGroup(Player)
    if (tonumber(string.byte(Player.id)) % 2 == 0) then
        return "A"
    else
        return "B"
    end
end

